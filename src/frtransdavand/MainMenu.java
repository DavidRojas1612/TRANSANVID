    private void insertarchoferActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_insertarchoferActionPerformed
        String cedula = ICedula.getText();
        String nombre = INombrech.getText();
        String celular = ICelular.getText();

        String apellido1 = IApellido1.getText();
        String apellido2 = IApellido2.getText();
        String contrasena = LContrasena.getText();

        LogicaDeNegocio ln = new LogicaDeNegocio();
        if (!ln.validarSiExistech(cedula)) {

            cono.insertarCh(cedula, nombre, apellido1, apellido2, celular);
            cono.insertarchlg(cedula, contrasena);

        } else {

            JOptionPane.showMessageDialog(this, "Chofer ya existe");

            FICHOFER.requestFocusInWindow();

            return;

        }


    }//GEN-LAST:event_insertarchoferActionPerformed

