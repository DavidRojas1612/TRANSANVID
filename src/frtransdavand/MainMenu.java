    public void ListarDatos() throws ClassNotFoundException {
        viaje = cone.ListViajes();
        DefaultTableModel tb = (DefaultTableModel) TablaViaje.getModel();
        for (viajes ch : viaje) {
            tb.addRow(new Object[]{ch.getNombre(), ch.getCedula(), ch.getPlaca(), ch.getFecha_v(), ch.getRuta_codigo_r(), ch.getMun_origen(), ch.getMun_destino()});

        }
    }

    public void ListarChofer() throws ClassNotFoundException {
        chofer = cono.LisChofer();
        DefaultTableModel tb = (DefaultTableModel) TablaChofer.getModel();
        for (chofer ch : chofer) {
            tb.addRow(new Object[]{ch.getCedula(), ch.getNombre(), ch.getApellido1(), ch.getApellido2(), ch.getCelular()});

        }
    }

    
    public void Listarbus() throws ClassNotFoundException {
        bbus = conu.LisBuses();
        DefaultTableModel tb = (DefaultTableModel) TablaBus.getModel();
        for (buses bs : bbus) {
            tb.addRow(new Object[]{bs.getPlaca(), bs.getMarca()});

        }
    }
    
    public void LimpiarFormulario() {
        DefaultTableModel tb = (DefaultTableModel) TablaViaje.getModel();
        for (int i = tb.getRowCount() - 1; i >= 0; i--) {
            tb.removeRow(i);
        }

    }

