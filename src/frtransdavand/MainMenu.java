    private void BInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BInsertActionPerformed
        String cedula = TCedula.getText();
        int ruta = Integer.parseInt(TRuta.getText());
        String placa = TPlaca.getText();
        String fecha = new SimpleDateFormat("dd/MM/yyyy").format(Fecha.getDate());
        String fecha1 = new SimpleDateFormat("yyyy/MM/dd").format(Fecha.getDate());

        LogicaDeNegocio ln = new LogicaDeNegocio();
        if (!ln.validarSiExistevj(cedula, ruta, placa, fecha1)) {
              if (ln.validarSiExistech(cedula)) {
                 cone.insertar(cedula, ruta, placa, fecha);
                  TCedula.setText("");
                 TRuta.setText("");
                 TPlaca.setText("");
                  Fecha.setToolTipText("");
        

             } else {

            JOptionPane.showMessageDialog(this, "Chofer no valido, por favor revice la cedula!");

            FICHOFER.requestFocusInWindow();

            return;

            }
          

            JOptionPane.showMessageDialog(this, "viaje programado correctamente!");
        } else {

            JOptionPane.showMessageDialog(this, "error, el viaje ya estÃ¡ programado");

            FICHOFER.requestFocusInWindow();

            return;

        }


    }//GEN-LAST:event_BInsertActionPerformed

